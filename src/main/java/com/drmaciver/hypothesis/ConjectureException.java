package com.drmaciver.hypothesis;

public class ConjectureException extends RuntimeException {

	/**
	 *
	 */
	private static final long serialVersionUID = 1L;

	public ConjectureException() {
		super();
	}

	public ConjectureException(String arg0) {
		super(arg0);
	}

	public ConjectureException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ConjectureException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public ConjectureException(Throwable arg0) {
		super(arg0);
	}

}
